class Genome:
    """The set of DNA sequences specifying an organism,
    plus higher order features derived from that sequence
    (e.g. genes, repeats, transposons, etc.).  Also,
    higher order features resulting from external designs
    based on the sequence (e.g. microarray oligo sets)."""

    class Error(BaseException):
        def __init__(self, e = "Genome Error"):
            self.e = e
        def __repr__(self):
            return self.e
        def __str__(self):
            return self.e

    def __init__(self, name, contigs, genes):
        """Init from .GenomeAssembly and GeneSet."""
        self.name = name
        self.contigs = contigs
        self.genes = genes
        self.gene_index = None
        # Removed genes.  This will probably be moved to GeneSet
        self.removed = []
        # Added genes.  This will probably be moved to GeneSet
        self.new_genes = []
        
    @property
    def genetic_code(self):
        """Return name of alternate genetic code or None for universal
        genetic code."""
        return None

    def Name(self):
        return self.name

    # Replace Genes() and Contigs() with iterators?

    def Genes(self):
        """Iterator over all genes in the genome."""
        return self.genes.items()

    def getGene(self, name):
        """Get a gene by name lookup.
        Name will be cleaned up based on gene set specific oddities.
        Returns None if no gene is found.
        Throws an exception on ambiguous name matching.
        """
        try:
            try:
                name = genefix[self.Name()](name)
            except:
                pass
            
            return self.genes[name]
        except KeyError:
            return None

    def get_gene_by_sha(self, sha):
        """Get a gene by the first 12 hexidecimal digits of the sha1 digest of
        its name (e.g., as generated by Blastxml2inparanoid.py and
        InparanoidCrunch.py)
        """
        try:
            return self.__sha2gene[sha]
        except AttributeError:
            if(self.genes is None):
                self._initGenes()
            self.__sha2gene = dict((i.sha1_12(),i) for i in self.genes.values())

            return self.__sha2gene[sha]

    def getGenesByLocus(self, locus):
        """Return a list of genes with _any_ overlap to locus."""
        if(self.gene_index is None):
            self.gene_index = SortLoci(self.genes.values())
        
        return RefCollisions((locus,), self.gene_index).OverlapA(locus)
        
    def Contigs(self):
        """Iterator over all contigs in the genome."""
        # TODO: make this more iterator-like?
        return self.contigs.items()

    def GetContig(self, name):
        """Get a contig by name lookup."""
        return self.contigs[name]

    def GetSequence(self, locus):
        """Returns the genomic sequence corresponding to locus.
        If locus extends past either edge of the contig given
        by locus.ref, sequence is returned for locus truncated
        to the contig edges (analogous to string.__getslice__)."""

        # We implement 5' clipping here to avoid issues with negative
        # slicing indices.  3' clipping is currently implimented downstream
        # do avoid requiring (possibly expensive) implementation of __len__
        # for contigs.  TODO: rethink this.
        seq = self.contigs[locus.ref][max(0,locus.start - 1):locus.stop]
        
        if(locus.strand == "-"):
            return seq.Complement()

        return seq

    def Translate(self, seq, from_start = False):
        """Translate Sequence using any special translation rules
        for this Genome.  Defaults to letting the sequence translate
        itself (which is not necessarily the same as defaulting to
        the universal genetic code)."""
        return seq.Translate(genetic_code = self.genetic_code,
                             from_start = from_start)

    def writeGbrowse2Gff3(self, fout, source = "GeneSet"):
        """Write the coordinates of the gene set to fout
        in Gbrowse2-compatible GFFv3 format.
        (See MemGeneSet.fromGbrowse2Gff3 for a detailed
         specification of this format).
        Gene order in the file and CDS numbering follows lexical sort of
        gene names.
        """
        cdsID = 0

        for (name, gene) in sorted(self.Genes()):
            cdsID = gene.writeGbrowse2Gff3(fout, cdsID = cdsID, source = source)

    def writeFlairGtf(self, fout, source = "GeneSet"):
        """Write the coordinates of the gene set to fout
        in FLAIR-compatible GTF format
        Gene order in the file and CDS numbering follows lexical sort of
        gene names.
        """
        cdsID = 0

        for (name, gene) in sorted(self.Genes()):
            cdsID = gene.writeFlairGtf(fout, cdsID = cdsID, source = source)
            
    def protein_fasta_stream(self):
        print("Dumping FASTA")
        fname = "Lucien2_hack.%s.fasta" % self.name
        fp = open(fname,"w")
        for (name, gene) in self.Genes():
            if(len(gene.Cds()) > 0):
                fp.write(gene.ProteinSequence().FormatFasta(name = name))
        print("Dumped it")
        return open(fname)
            
