class GeneSet:
    """A set of Genes.  Provides support for timestamped addition,
    removal, and modification of genes.  At this level, the interface
    is read-only."""
    class Error(BaseException):
        def __init__(self, e = "GeneSet error"):
            self.e = e
        def __repr__(self):
            return self.e
        def __str__(self):
            return self.e

    def __getitem__(self, i):
        raise GeneSet.Error("__getitem__ not implemented")

    def __len__(self):
        raise GeneSet.Error("__len__ not implemented")

    def items(self):
        raise GeneSet.Error("items not implemented")

    def __iter__(self):
        for (name, gene) in self.items():
            yield gene

    def AddGenes(self, newgenes = [], timestamp = None):
        """Add a list of genes to the set.  Genes with the same name
        as existing genes in the set will be treated as updates and
        will overwrite them."""
        raise GeneSet.Error("AddGenes not implemented")

class MemGeneSet(GeneSet):
    """In-memory implementation of GeneSet.  Adds write access to
    the GeneSet."""
    def __init__(self):
        """Create an empty MemGeneSet."""
        # Note: assumes uniquely named genes.  This is a problem for
        #       broken gene sets (e.g. Y. lippolytica) but perhaps
        #       we don't mind.
        self.genes = {}

    def __getitem__(self, i):
        # Note: any name fixing should be implemented at a higher level.
        return self.genes[i]

    def __len__(self):
        return len(self.genes)

    def items(self):
        return self.genes.items()

    def values(self):
        return self.genes.values()
    
    def AddGenes(self, newgenes = [], timestamp = None):
        """Add a list of genes to the set.  Genes with the same name
        as existing genes in the set will be treated as updates and
        will overwrite them."""
        for i in newgenes:
            self.genes[i.Name()] = i

        # TODO: Check for collisions.  Where collisions result in
        #       a change (genes[i.Name()] != i) make a note in the
        #       timestamped changelog.  Where there is no collision,
        #       make a note in the timestamped add log.

        # Note that most of the logic for updating a GSC set from a
        # FASTA file is pushed up to the client.

        # Note that this implementation allows newgenes to be a heterogenous
        # list of Gene subclasses (e.g., the client can mix in memory
        # instances with database references).  I think that this is fine.
        # It may turn out that this implementation is also ok for DbGeneSet.
        # Otherwise, DbGeneSet will need to make explicit use of the Gene
        # interface to switch between Gene representations --> this may
        # be best done by giving the concrete Gene classes copy constructors
        # based on the base class interface.

    @classmethod
    def fromGbrowse2Gff3(cls, fp, genome = None):
        """Initialize a gene set from fp, a GFFv3 formatted file stream.
        
        There are multiple conventions for representing gene sets as GFF
        (BROAD GTF, CGD/AspGD, SGD, Gbrowse2, ...)
        This function assumes the Gbrowse2 convention where:
          Each gene has one "gene" feature, spanning all sub-features.
          The name of the gene is stored in the "Name" attribute of the
            "gene" feature.
          There is one "exon" feature per exon.
          There is one "cds" feature per disjoint coding sequence on the genome.
          All "cds" have a phase of 0 (i.e., they are collectively 1 ORF)
          All sub-features give the "gene" feature as their parent.
        """
        # Parse and index features
        gff = Gff3file.fromFile(fp)

        genes = []
        
        for gene in gff.type_to_features["gene"]:
            locus = gene.Locus()
            ref = locus.ref
            start = locus.start
            stop = locus.stop
            strand = locus.strand
            name = gene.attributes["Name"][0]

            if(locus.strand == "+"):
                transform = lambda x : (x.Locus().start - start + 1,
                                        x.Locus().stop - start + 1)
            else:
                transform = lambda x : (stop - x.Locus().stop + 1,
                                        stop - x.Locus().start + 1)

            # exons relative to "gene" coordinates
            exons = [transform(i) for i in gff.children[gene]
                     if(i.type == "exon")]
            
            # CDS relative to "gene" coordinates
            t_cds = [transform(i) for i in gff.children[gene]
                     if(i.type == "cds")]

            # Map cds to "transcript" coordinates based on exons

            # Assumption: Each CDS feature is completely contained by one
            #             and only one exon.

            cds = []
            offset = 0
            for i in exons:
                for j in range(len(t_cds) - 1, -1, -1):
                    if((i[0] <= t_cds[j][0]) and
                       (i[1] >= t_cds[j][1])):
                        new_cds = (t_cds[j][0] - i[0] + offset + 1,
                                   t_cds[j][1] - i[0] + offset + 1)
                        # Fuse CDS features that are adjacent in the transcript
                        if((len(cds) > 0) and (new_cds[0] == cds[-1][1]+1)):
                            assert(cds[-1][0] < new_cds[0] <= new_cds[1])
                            cds[-1] = (cds[-1][0], new_cds[1])
                        else:
                            cds.append(new_cds)
                        t_cds = t_cds[:j]+t_cds[j+1:]
                offset += i[1] - i[0] + 1
            if(len(t_cds) > 0):
                raise IndexError("%d CDS not contained in exons" % (
                    len(t_cds)))

            genes.append(MemGene(name = name,
                                 locus = Locus(ref = ref, 
                                               start = start,
                                               stop = stop,
                                               strand = strand,
                                               genome = genome),
                                 exons = exons,
                                 cds = cds))
        geneset = cls()
        geneset.AddGenes(genes)
        return geneset

class Contig:
    def __init__(self, name, length, assembly = None, genome = None):
        self.name = name
        self.length = int(length)
        self.assembly = assembly
        self.genome = genome
        self._seq = None

    def getslice(self, i, j):
        if(self._seq is None):
            self._initSeq()
        return self._seq[i:j]

    def __getitem__(self, i):
        if(isinstance(i, slice)):
            return self.getslice(i.start, i.stop)
        if(self._seq is None):
            self._initSeq()
        return self._seq[i]

    def __str__(self):
        return str(self[0:self.length])

    def __len__(self):
        return self.length

    def Complement(self):
        return str(DnaSequence(str(self)).Complement())

    def _initSeq(self):
        self._seq = self.assembly[self.name]

    def Locus(self):
        return Locus(ref = self.name, start = 1, stop = len(self),
                     strand = "+", genome = self.genome)
    
