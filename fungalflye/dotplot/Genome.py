        if(locus.strand == "+"):
            ex = [(i.start - locus.start + 1, i.stop - locus.start + 1)
                  for i in exon_loci]
        else:
            ex = [(locus.stop - i.stop + 1, locus.stop - i.start + 1)
                  for i in exon_loci]

        return MemGene(name = name, locus = locus, exons = sorted(ex))

