class Gene:
    class Error(BaseException):
        def __init__(self, e = "Gene error"):
            self.e = e
        def __repr__(self):
            return self.e
        def __str__(self):
            return self.e

    # Two genes are the same if they have the same name.
    # I.e., we assume that names are sufficiently unique
    # such that equivalent names implies equivalent loci,
    # gene structure, and sequence.  Given that much of
    # the system depends on name based lookup, this is
    # a reasonable assumption.

    def __eq__(self, rhs):
        if(isinstance(rhs, Gene)):
            return self.Name() == rhs.Name()
        else:
            return 0

    # If equality is defined on gene name, the sorting genes
    # should give a lexical sort

    def __le__(self, rhs):
        return self.Name().__le__(rhs.Name())
    def __lt__(self, rhs):
        return self.Name().__lt__(rhs.Name())
    def __ge__(self, rhs):
        return self.Name().__ge__(rhs.Name())
    def __gt__(self, rhs):
        return self.Name().__gt__(rhs.Name())
    def __ne__(self, rhs):
        if(isinstance(rhs, Gene)):
            return self.Name() == rhs.Name()
        else:
            return True
            
    def __hash__(self):
        return hash(str(self.Name()))

    def __repr__(self):
        return self.Name()

    @property
    def long_name(self):
        """Tuple of (genome name, gene name) strings.  Intended as
        unique identifier (which will be the case if gene names are
        unique within genomes and if genome names are globally unique).

        Raises exception for genes not associated with genomes.
        """
        return (str(self.Locus().genome.name),str(self.Name()))

    def Name(self):
        """Gene name as string"""
        raise Gene.Error("Name not implemented")

    def sha1_12(self):
        """Return first 12 (hex) characters of SHA1 digest of name.

        This is a safe stable name to use for programs such as
        CLUSTAL, INPARANOID, and MochiView that can't handle
        arbitrarily long gene names."""

        # TODO: Any global name-normalizing rules should go here
        #       (e.g., fixes for "final" vs. "final_new" in G186AR (GSC))
        
        return sha1(self.Name().encode()).hexdigest()[:12]

    def Locus(self):
        """Return locus struct"""
        raise Gene.Error("Locus not implemented")

    def Exons(self):
        """Return exons as (first, last) list relative to genomic seq."""
        raise Gene.Error("Exons not implemented")

    def ExonLoci(self):
        """Return exons as list of loci relative to contig."""
        locus = self.Locus()
        if(locus.strand == "-"):
            return [Locus(ref = locus.ref,
                          start = locus.stop - last + 1,
                          stop = locus.stop - first + 1,
                          strand = "-",
                          genome = locus.genome)
                    for (first, last) in self.Exons()]
        else:
            return [Locus(ref = locus.ref,
                          start = locus.start + first - 1,
                          stop = locus.start + last - 1,
                          strand = "+",
                          genome = locus.genome)
                    for (first, last) in self.Exons()]
        
    def Cds(self):
        """Return cds regions as (first, last) list relative to transcript seq."""
        raise Gene.Error("Cds not implemented")

    def CdsLoci(self):
        """Return cds regions as a list of Locus instances relative to the
        genomic sequence, but sorted relative to their order of transcription
        (i.e., minus strand transcripts have their CdsLoci ordered right
        to left).
        """
        # Map exons to transcript coordinates, retaining back references
        # to genome coordinates
        
        class MappedExon:
            def __init__(self, tloc, gloc):
                self.tloc = tloc
                self.gloc = gloc
            def Locus(self):
                return self.tloc.Locus()
        
        locus = self.Locus()
        texons = []
        tpos = 1
        for exon in self.ExonLoci():
            npos = tpos + len(exon) - 1
            texons.append(MappedExon(
                Locus(ref = "transcript",
                      start = tpos,
                      stop = npos,
                      strand = "+",
                      genome = None),
                exon))
            tpos = npos+1

        # Take the intersection of the exons with the cds in
        # transcript space, and return the corresponding loci
        # in genome space.  Currently, we do an exhaustive
        # comparison of all exons to all cds.  There should
        # be a more elegant solution given both as sorted lists...

        retval = []
        for (cstart, cstop) in self.Cds():
            cloc = Locus(ref = "transcript",
                         start = cstart,
                         stop = cstop,
                         strand = "+",
                         genome = None)

            for exon in texons:
                overlap = exon.Locus().Overlap(cloc)
                # Case 1: no overlap --> ignore this exon wrt this cds
                if(overlap < 1):
                    continue
                # Case 2: full overlap --> this full exon propagates to output
                elif(overlap == len(exon.Locus())):
                    retval.append(exon.gloc)
                # Case 3: partial overlap --> make corresponding slice in
                #         genome space
                else:
                    if(cloc.start > exon.tloc.start):
                        start = cloc.start - exon.tloc.start + 1
                    else:
                        start = 1
                    if(cloc.stop < exon.tloc.stop):
                        # N.B.: overlap implies cloc.stop >= tloc.start
                        stop = cloc.stop - exon.tloc.start + 1
                    else:
                        stop = len(exon.tloc)

                    if(exon.gloc.strand == "+"):
                        retval.append(
                            Locus(ref = exon.gloc.ref,
                                  start = exon.gloc.start + start - 1,
                                  stop = exon.gloc.start + stop - 1,
                                  strand = "+",
                                  genome = exon.gloc.genome))
                    else:
                        retval.append(
                            Locus(ref = exon.gloc.ref,
                                  start = exon.gloc.stop - stop + 1,
                                  stop = exon.gloc.stop - start + 1,
                                  strand = "-",
                                  genome = exon.gloc.genome))
        return retval

    def utr_seqs(self):
        """Return a tuple of DnaSequences for the 5' and 3' UTR of
        this gene.  Return empty sequences for missing UTRs.  Raise a
        ValueError for non-coding genes.  If the gene contains a stop
        codon, it will be included in the 3' UTR.
        """
        return tuple(DnaSequence("".join(str(locus.Sequence())
                                         for locus in i))
                     for i in self.utr_loci())
    
    def utr_loci(self):
        """Return a tuple of lists of loci for the 5' and 3' UTR
        respectively.  If this gene contains a stop codon, that codon
        will be included in the 3' UTR.  If this gene contains no
        coding sequence, raise a ValueError.  As for ExonLoci and
        CdsLoci, the loci will be sorted in 5'->3' order."""
        
        exons = self.ExonLoci()
        cds = self.CdsLoci()
        if(len(cds) == 0):
            raise ValueError
            
        utr5 = []
        utr3 = []
        if(self.Locus().strand == "+"):
            for (n,exon) in enumerate(exons):
                if(cds[0].start > exon.stop):
                    utr5.append(exon)
                elif(cds[0].start == exon.start):
                    break
                else:
                    utr5.append(
                        Locus.fromPrototype(exon, stop = cds[0].start-1))
                    break
            else:
                raise AssertionError("CDS don't overlap exons")
            for exon in exons[n:]:
                if(cds[-1].stop >= exon.stop):
                    continue
                elif(cds[-1].stop >= exon.start):
                    assert(len(utr3) == 0)
                    utr3.append(
                        Locus.fromPrototype(exon, start = cds[-1].stop+1))
                else:
                    utr3.append(exon)
            
        else: # minus strand
            # cds ands exons are both sorted 5'->3'
            for (n,exon) in enumerate(exons):
                if(cds[0].stop < exon.start):
                    utr5.append(exon)
                elif(cds[0].stop == exon.stop):
                    break
                else:
                    utr5.append(
                        Locus.fromPrototype(exon, start = cds[0].stop+1))
                    break
            else:
                raise AssertionError("CDS don't overlap exons")
            for exon in exons[n:]:
                if(cds[-1].start <= exon.start):
                    continue
                elif(cds[-1].start <= exon.stop):
                    assert(len(utr3) == 0)
                    utr3.append(
                        Locus.fromPrototype(exon, stop = cds[-1].start-1))
                else:
                    utr3.append(exon)

        return (utr5,utr3)
                    
    def SetExons(self, exons):
        """Set exons to (first, last) list."""
        raise Gene.Error("SetExons not implemented")

    def SetCds(self, cds):
        """Set cds to (first, last) list."""
        raise Gene.Error("SetCds not implemented")

    def GenomicSequence(self):
        """Return genomic sequence."""
        # Note: consider adding lazy evaluation at the genome level
        return self.Locus().Sequence()

    def TranscriptSequence(self):
        """Return transcript sequence."""
        # TODO: move this to a "splice" function at the Sequence level
        return DnaSequence(
            "".join(map(lambda x : str(self.GenomicSequence()[x[0]-1:x[1]]),
                        self.Exons())))

    def CodingSequence(self, add_stop = False):
        """Return cds sequence."""
        seq = "".join(map(lambda x : str(self.TranscriptSequence()[x[0]-1:x[1]]),
                          self.Cds()))
        if(add_stop):
            if(not self.has_stop_codon):
                raise ValueError("No stop codon for %s!" % (self.Name()))
            seq += str(self.StopCodonSequence())
            
        return DnaSequence(seq)

    def StopCodonSequence(self):
        """Return the codon immediately following the last CDS base in
        this transcript.  Don't check if the returned value is a valid
        stop codon.  Return zero to two bases for a truncated stop.
        """
        last = self.Cds()[-1][1]
        return self.TranscriptSequence()[last:last+3]

    def has_stop_codon(self):
        """Return True if the codon immediately following the last CDS base
        in this transcript would be translated as a stop codon in the native
        genome.
        """
        return str(self.Locus().genome.Translate(self.StopCodonSequence())
                   ) == "*"

    def ProteinSequence(self):
        """Return translated cds sequence.  May include internal stops."""
        # Note: We let Genome do the translation so that different
        #       species can use different genetic codes
        #       We set from_start to handle non-canonical initiators
        #       (e.g. C. albicans)
        return self.Locus().genome.Translate(self.CodingSequence(),
                                             from_start = True)

    def splice_junctions(self):
        """Return a list of four character splice junction sequences."""
        seqs = []
        exon_loci = self.ExonLoci()
        for i in range(len(exon_loci) - 1):
            l1 = exon_loci[i]
            l2 = exon_loci[i+1]
            if(self.Locus().strand == "+"):    
                seq = (str(Locus.fromPrototype(
                    l1, start = l1.stop+1, stop = l1.stop+2).Sequence())+
                       str(Locus.fromPrototype(
                    l2, start = l2.start-2, stop = l2.start-1).Sequence()))
            else:
                # Note that strand of exon locus automatically gives
                # complementation
                seq = (str(Locus.fromPrototype(
                    l1, start = l1.start-2, stop = l1.start-1).Sequence())+
                       str(Locus.fromPrototype(
                    l2, start = l2.stop+1, stop = l2.stop+2).Sequence()))
            seqs.append(seq)
        return seqs

    def writeGbrowse2Gff3(self, fout, cdsID = 0, source = "GeneSet"):
        """Write the coordinates of this to fout
         in Gbrowse2-compatible GFFv3 format.
         (See MemGeneSet.fromGbrowse2Gff3 for a detailed
          specification of this format)
         The cdsID parameter gives the highest cdsID currently used in fout.
         Return the last cdsID for this gene.
         (N.B.: Current model is that every gene has _exactly_ one reading
          frame, generated by concatenating the CDS features)
         """
        # transcript
        fout.write("\t".join((
            self.Locus().ref,
            source,
            "gene",
            "%d" % self.Locus().start,
            "%d" % self.Locus().stop,
            ".",
            self.Locus().strand,
            ".",
            "ID=%s;Name=%s" % (self.Name(),self.Name())))+"\n")

        # exons
        for exon in self.ExonLoci():
            fout.write("\t".join((
                exon.Locus().ref,
                source,
                "exon",
                "%d" % exon.Locus().start,
                "%d" % exon.Locus().stop,
                ".",
                exon.Locus().strand,
                ".",
                "Parent=%s" % self.Name()))+"\n")

        # cds
        cdsID += 1
        for cds in self.CdsLoci():
            fout.write("\t".join((
                cds.Locus().ref,
                source,
                "cds",
                "%d" % cds.Locus().start,
                "%d" % cds.Locus().stop,
                ".", # score
                cds.Locus().strand,
                "0", # phase
                "ID=cds%07d;Parent=%s" % (cdsID,self.Name())))+"\n")

        return cdsID

    def writeFlairGtf(self, fout, cdsID = 0, source = "GeneSet"):
        """Write the coordinates of this gene to fout
        in FLAIR-compatible GTF format.
        """

        # transcript
        fout.write("\t".join((
            self.Locus().ref,
            source,
            "gene",
            "%d" % self.Locus().start,
            "%d" % self.Locus().stop,
            ".",
            self.Locus().strand,
            ".",
            'trancript_id "%s"' % self.Name()))+"\n")

        # exons
        for exon in self.ExonLoci():
            fout.write("\t".join((
                exon.Locus().ref,
                source,
                "exon",
                "%d" % exon.Locus().start,
                "%d" % exon.Locus().stop,
                ".",
                exon.Locus().strand,
                ".",
                'transcript_id "%s"' % self.Name()))+"\n")

        # cds
        cdsID += 1
        for cds in self.CdsLoci():
            fout.write("\t".join((
                cds.Locus().ref,
                source,
                "CDS",
                "%d" % cds.Locus().start,
                "%d" % cds.Locus().stop,
                ".", # score
                cds.Locus().strand,
                "0", # phase
                'ID=cds%07d;transcript_id "%s"' % (cdsID,self.Name())))+"\n")

        return cdsID
    
